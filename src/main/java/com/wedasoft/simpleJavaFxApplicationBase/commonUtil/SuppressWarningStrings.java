package com.wedasoft.simpleJavaFxApplicationBase.commonUtil;

@SuppressWarnings({"all", "unused", "FieldMayBeFinal", "FieldCanBeLocal"})
public class SuppressWarningStrings {

    /**
     * This suppresses all warnings.
     */
    public static final String ALL = "all";

    /**
     * This suppresses warnings related to busy waiting.
     */
    public static final String BUSY_WAIT = "BusyWait";

    /**
     * This suppresses warnings related to object cast operations.
     */
    public static final String CAST = "cast";

    /**
     * This suppresses warnings related to constant conditions, like "this variable is always null".
     */
    public static final String CONSTANT_CONDITIONS = "ConstantConditions";

    /**
     * This suppresses warnings related to attribute fields that can be local variables.
     */
    public static final String FIELD_CAN_BE_LOCAL = "FieldCanBeLocal";

    /**
     * This suppresses warnings related to non final fields that may be final.
     */
    public static final String FIELD_MAY_BE_FINAL = "FieldMayBeFinal";

    /**
     * This suppresses warnings for fields which may be static.
     */
    public static final String FIELD_MAY_BE_STATIC = "FieldMayBeStatic";

    /**
     * This suppresses warnings related to instantiations of utility classes.
     */
    public static final String INSTANTIATION_OF_UTILITY_CLASS = "InstantiationOfUtilityClass";

    /**
     * This suppresses warnings related to the usage of raw types when working with generics.
     */
    public static final String RAW_TYPES = "rawtypes";

    /**
     * This suppresses warnings of unused code.
     */
    public static final String UNUSED = "unused";

}
